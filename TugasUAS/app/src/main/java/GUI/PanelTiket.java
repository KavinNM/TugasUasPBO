    public void refresh() {
        dataPerjalanan = daoPerjalanan.selectAll();
        tblPerjalanan.setModel(new TabelPerjalanan(dataPerjalanan));
        dataTiket = dao.selectAll();
        tblTiket.setModel(new TabelTiket(dataTiket));
        txtIdTiket.setEnabled(true);
        clear();
    }

    private Perjalanan getPerjalananById(String id) {
        for (Perjalanan perjalanan : dataPerjalanan) {
            if (perjalanan.idperjalanan.equals(id)) {
                return perjalanan;
            }
        }
        return null;
    }

