    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        txtIdTiket.setEnabled(true);
        String idperjalanan = txtIdPerjalanan.getText();
        String idtiket = txtIdTiket.getText();
        int jumlah = Integer.parseInt(txtJumlah.getText());
        Perjalanan perjalanan = getPerjalananById(idperjalanan);
        int harga = perjalanan.toharga();
        Tiket newtiket = new Tiket(idtiket, jumlah,harga);
        newtiket.harga=harga;
        newtiket.idperjalanan = perjalanan;

        if (editMode == false) {
            dao.insert(newtiket);
        } else {
            dao.update(newtiket);
        }

        refresh();
        clear();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblPerjalananMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPerjalananMouseClicked
        if (evt.getClickCount() == 2) {
            txtIdTiket.setEnabled(true);

            Perjalanan perjalanan = dataPerjalanan.get(tblPerjalanan.getSelectedRow());

            txtIdPerjalanan.setText(perjalanan.idperjalanan);
            txtIdPerjalanan.setEnabled(false);

        }
    }//GEN-LAST:event_tblPerjalananMouseClicked

    private void tblTiketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTiketMouseClicked
        if (evt.getClickCount() == 2) {
            editMode = true;
            btnHapus.setEnabled(true);

            Tiket tiket = dataTiket.get(tblTiket.getSelectedRow());

            txtIdPerjalanan.setText(tiket.idperjalanan.idperjalanan);
            txtIdTiket.setText(tiket.idtiket);
            txtJumlah.setText(String.valueOf(tiket.jumlah));

            txtIdPerjalanan.setEnabled(false);
            txtIdTiket.setEnabled(false);

        }
    }//GEN-LAST:event_tblTiketMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        txtIdTiket.setEnabled(true);

        dao.delete(txtIdTiket.getText());
        refresh();
    }//GEN-LAST:event_btnHapusActionPerformed


